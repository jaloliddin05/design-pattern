/*
Structural design patterns are a category of design patterns that focus on simplifying the organization and composition of classes or 
objects to form larger structures. These patterns help define the relationships between different components, making it easier to build 
flexible and reusable software systems. Structural patterns primarily deal with the composition of classes and objects to create larger
 structures while keeping the system modular and scalable.

Here are some commonly used structural design patterns:

1. **Adapter Pattern:**
   - Allows the interface of an existing class to be used as another interface, making it compatible with a client's expectations.

2. **Bridge Pattern:**
   - Separates abstraction from implementation so that both can vary independently. It involves a bridge interface that helps in connecting different components.

3. **Composite Pattern:**
   - Composes objects into tree structures to represent part-whole hierarchies. Clients can treat individual objects and compositions of objects uniformly.

4. **Decorator Pattern:**
   - Attaches additional responsibilities to an object dynamically. Decorators provide a flexible alternative to subclassing for extending functionality.

5. **Facade Pattern:**
   - Provides a simplified, unified interface to a set of interfaces in a subsystem. It defines a higher-level interface that makes the subsystem easier to use.

6. **Flyweight Pattern:**
   - Minimizes memory usage or computational expenses by sharing as much as possible with related objects. It is used to support a large number of 
   similar objects efficiently.

7. **Proxy Pattern:**
   - Provides a surrogate or placeholder for another object to control access to it. It involves creating a representative object that controls 
   access to another object.

These structural patterns are not meant to solve algorithmic problems or define specific algorithms. Instead, they address how classes and objects 
can be combined to form larger structures, emphasizing the relationships and composition of software components. The use of structural design 
patterns often leads to more modular, maintainable, and extensible code.
*/